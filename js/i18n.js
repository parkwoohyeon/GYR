// 국가→언어 매핑
const COUNTRY_LANG_MAP = {
  vi:'vi', tl:'tl', uz:'uz', km:'km', id:'id', ne:'ne', my:'my', si:'si', th:'th', en:'en'
};

// ======================================================================
// 현재 언어 상태 + 번역 헬퍼 함수 + 언어 적용 함수
// ⚠️ currentLang 변수는 직접 수정하지 마세요.
//    changeLang() 또는 selectCountry() 함수를 통해서만 변경됩니다.
// ⚠️ applyLang() 함수 내부 로직 수정 금지 — 번역 전체가 깨집니다.
// ======================================================================
let currentLang = localStorage.getItem('wb_lang') || 'ko';

function t(key) {
  return (i18n[currentLang] && i18n[currentLang][key]) || (i18n['ko'] && i18n['ko'][key]) || key;
}

function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (val !== key) el.textContent = val;
  });
  document.querySelectorAll('[data-i18n-nav]').forEach(el => {
    const key = el.getAttribute('data-i18n-nav');
    const val = t(key);
    if (val !== key) el.textContent = val;
  });
  const hEl = document.getElementById('hero-headline');
  if (hEl) hEl.innerHTML = t('hero_headline');
  const sEl = document.getElementById('hero-sub');
  if (sEl) sEl.textContent = t('hero_sub');
  renderEduCards();
  renderJobCards(getFilteredJobs());
  // 셀렉트박스 동기화
  const dLang = document.getElementById('desktop-lang');
  const mLang = document.getElementById('mobile-lang');
  // 지원하는 옵션 값 체크
  const supportedInSelect = ['ko','en','vi','tl','uz','km','id','ne','my','si','th'];
  if (dLang && supportedInSelect.includes(currentLang)) dLang.value = currentLang;
  if (mLang && supportedInSelect.includes(currentLang)) mLang.value = currentLang;
  // 챗봇 힌트/placeholder 갱신
  initChatbot();
}

function changeLang(lang) {
  currentLang = lang;
  localStorage.setItem('wb_lang', lang);
  applyLang();
}

// ==============================
// [신규] 국가 선택 기능
// ==============================
function selectCountry(countryCode) {
  const lang = COUNTRY_LANG_MAP[countryCode] || 'en';
  // 버튼 강조
  document.querySelectorAll('.country-btn').forEach(b => {
    b.classList.toggle('selected', b.getAttribute('data-country') === countryCode);
  });
  // 언어 전환
  currentLang = lang;
  localStorage.setItem('wb_lang', lang);
  localStorage.setItem('wb_country', countryCode);
  // 페이드 전환
  document.body.classList.add('lang-fade');
  setTimeout(() => { document.body.classList.remove('lang-fade'); }, 300);
  applyLang();
}

function restoreCountrySelection() {
  const saved = localStorage.getItem('wb_country');
  if (saved) {
    document.querySelectorAll('.country-btn').forEach(b => {
      b.classList.toggle('selected', b.getAttribute('data-country') === saved);
    });
  }
}